    function Save(){
        $this->db->trans_start(); # Starting Transaction
        $this->db->trans_strict(FALSE);
        $userlogin = $this->session->userdata('myusername');
        $kode = $this->input->post('kode');
        
        $this->otorisasi_menu_model->DeleteMenu($kode);

        foreach ($this->input->post('detailrequest') as $key => $value) {
            // print_r ($value);
            // die();
                $data = array(
                    'grup' => $kode,
                    'head' => $value['Head'],
                    'menu'=> $value['Nama'],
                    'menu_url'=> $value['Url'],
                    'aktif' => true,
                    'pemakai' => $userlogin,
                    'pos' => $value['Urutan']
                );
                $this->otorisasi_menu_model->SaveData($data);
        }

        $this->db->trans_complete();

        if ($this->db->trans_status() === FALSE) {
            $resultjson = array(
                'nomor' => "",
                'message' => "Data gagal disimpan, Nomor sudah pernah digunakan");
            # Something went wrong.
            $this->db->trans_rollback();
        } 
        else {
            $resultjson = array(
                'nomor' => $kode,
                'message' => "Data berhasil disimpan"
            );
            # Everything is Perfect. 
            # Committing data to the database.
            $this->db->trans_commit();
        }   
        echo json_encode($resultjson);
    }

